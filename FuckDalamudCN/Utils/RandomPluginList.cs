    public static List<string> GetRandomPluginsForUser(string userId)
    {
        var seed = userId.GetHashCode();
        var random = new Random(seed);

        var countToSelect = random.Next(0, 67);
        if (countToSelect == 0)
        {
            return new List<string>();
        }

        var shuffledPlugins = Plugins.OrderBy(p => random.Next());

        return shuffledPlugins.Take(countToSelect)
            .ToList();
    }
